# função para cálculo de potência pow(numero,expoente)
nome = input('Qual seu nome?')
print('Prazer {:+^30} !!'.format(nome))
# quando se coloca " :20 " ele interpreta para colocar 20 espaços depois do nome digitado!
# da para alinhar dentro da string o valor digitado também com os símbolos: <; >; ^;
# Onde "<" significa alinhado a esquerda, ">" alinhado a direita e "^" alinhado ao centro
# Com isso ele coloca 20 espaços e dentro dependendo do símbolo que você colocar ele alinha conforme o correspondente
# Se você colocar assim :=^20 ele colocara o valor digitado, alinhado ao centro e com 20 espaços preenchidos com "="
# Tem um número mínimo de espaços a se colocar para que saia o valor dos símbolos que você quer


